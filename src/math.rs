pub fn reflect(v: Vec3, n: Vec3) -> Vec3 {
    v - n * 2.0 * v.dot(n)
}

pub type Point = Vec3;
pub type Color = Vec3;

impl Color {
    pub fn to_ppm_color(&self) -> String {
        let r = (self.x * 255.0) as u8;
        let g = (self.y * 255.0) as u8;
        let b = (self.z * 255.0) as u8;
        format!("{} {} {}", r, g, b)
    }
}
